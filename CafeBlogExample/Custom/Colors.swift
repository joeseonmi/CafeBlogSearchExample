import Foundation
import UIKit

extension UIColor {
    static var black: UIColor {
        return UIColor(red: 38/255, green: 38/255, blue: 38/255, alpha: 1)
    }
    
    static var darkGray: UIColor {
        return UIColor(red: 119/255, green: 119/255, blue: 119/255, alpha: 1)
    }
    
    static var lightGray: UIColor {
        return UIColor(red: 229/255, green: 229/255, blue: 229/255, alpha: 1)
    }
    
    static var red: UIColor {
        return UIColor(red: 220/255, green: 48/255, blue: 50/255, alpha: 1)
    }

}
